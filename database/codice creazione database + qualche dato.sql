	DECLARE sismografoId int default 0;
    DECLARE lastSismografoTime datetime;
    DECLARE saveData int default 0; 
	SET sismografoId = (SELECT MAX(ID_SISMOGRAFO) FROM shake);
    IF sismografoId is NULL THEN
